static uint8_t byte_at(const std::vector<uint8_t>& data, size_t off) {
    if (off >= data.size()) return 0;
    return data[off];
}

static std::string detect_layout(const std::vector<uint8_t>& data) {
    // These probes mirror the original Windows patcher's version checks.
    const bool a = byte_at(data, 0x13e5e) == 0x68 && byte_at(data, 0x13e63) == 0x68;
    const bool b = byte_at(data, 0x13e4e) == 0x68 && byte_at(data, 0x13e53) == 0x68;
    if (a && !b) return "a";
    if (b && !a) return "b";
    if (a) return "a";
    return "";
}

static void write_bytes(std::fstream& f, std::uint64_t off, const std::vector<uint8_t>& bytes) {
    f.seekp(static_cast<std::streamoff>(off), std::ios::beg);
    if (!f) throw std::runtime_error("seek failed at offset 0x" + std::to_string(off));
    f.write(reinterpret_cast<const char*>(bytes.data()), static_cast<std::streamsize>(bytes.size()));
    if (!f) throw std::runtime_error("write failed at offset 0x" + std::to_string(off));
}

static void write_zeroes(std::fstream& f, std::uint64_t off, size_t count) {
    write_bytes(f, off, std::vector<uint8_t>(count, 0));
}

static void write_float(std::fstream& f, std::uint64_t off, float value) {
    static_assert(sizeof(float) == 4, "32-bit float required");
    std::vector<uint8_t> bytes(4);
    std::memcpy(bytes.data(), &value, 4);
#if __BYTE_ORDER__ == __ORDER_BIG_ENDIAN__
    std::reverse(bytes.begin(), bytes.end());
#endif
    write_bytes(f, off, bytes);
}

static bool is_timer_init_pattern(const std::vector<uint8_t>& data) {
    // In the uploaded Game.exe this code initializes the frame timer:
    //   push 60.0f
    //   push 60.0f
    //   mov ecx, 0x62a4b8
    //   call 0x464e80
    // The immediate float payloads are at file offsets 0x1c6c21 and 0x1c6c26.
    const size_t off = 0x1c6c20;
    const std::vector<uint8_t> marker = {
        0x68, 0x00, 0x00, 0x70, 0x42,
        0x68, 0x00, 0x00, 0x70, 0x42,
        0xb9, 0xb8, 0xa4, 0x62, 0x00,
        0xe8
    };
    if (data.size() < off + marker.size()) return false;
    return std::memcmp(data.data() + off, marker.data(), marker.size()) == 0;
}

static bool is_timer_init_push_site(const std::vector<uint8_t>& data) {
    // Accept already-patched timer values too, as long as the surrounding push/mov/call site is intact.
    return data.size() > 0x1c6c30 &&
           data[0x1c6c20] == 0x68 &&
           data[0x1c6c25] == 0x68 &&
           data[0x1c6c2a] == 0xb9 &&
           data[0x1c6c2f] == 0xe8;
}

static void apply_fps_patch(std::fstream& f, const std::vector<uint8_t>& original, float fps_value, bool force) {
    if (fps_value <= 0.0f) throw std::runtime_error("FPS value must be positive");

    if (!is_timer_init_push_site(original)) {
        throw std::runtime_error("Could not find the expected frame-timer initialization site; FPS patch was not applied.");
    }

    if (!is_timer_init_pattern(original) && !force) {
        std::cout << "FPS timer site was found, but it no longer contains the original 60 FPS values; overwriting with requested FPS anyway. Use the .bak file if this was accidental.\n";
    }

    write_float(f, 0x1c6c21, fps_value);
    write_float(f, 0x1c6c26, fps_value);
}

static fs::path find_game_exe() {
    const std::vector<fs::path> candidates = {
        "Game.exe",
        "Game/Game.exe",
        "game.exe",
        "Game/game.exe"
    };
    for (const auto& p : candidates) {
        if (fs::exists(p)) return p;
    }
    return {};
}

static std::vector<uint8_t> read_all(const fs::path& p) {
    std::ifstream in(p, std::ios::binary);
    if (!in) throw std::runtime_error("Cannot open " + p.string());
    return std::vector<uint8_t>(std::istreambuf_iterator<char>(in), std::istreambuf_iterator<char>());
}

static void copy_backup(const fs::path& game) {
    fs::path backup = game;
    backup += ".bak";
    if (fs::exists(backup)) {
        std::cout << "Backup already exists: " << backup << "\n";
        return;
    }
    fs::copy_file(game, backup, fs::copy_options::none);
    std::cout << "Backup created: " << backup << "\n";
}

