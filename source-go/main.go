func byteAt(data []byte, off int) byte {
    if off < 0 || off >= len(data) {
        return 0
    }
    return data[off]
}

func detectLayout(data []byte) string {
    a := byteAt(data, 0x13e5e) == 0x68 && byteAt(data, 0x13e63) == 0x68
    b := byteAt(data, 0x13e4e) == 0x68 && byteAt(data, 0x13e53) == 0x68
    if a && !b { return "a" }
    if b && !a { return "b" }
    if a { return "a" }
    return ""
}

func writeBytes(f *os.File, off int64, b []byte) error {
    n, err := f.WriteAt(b, off)
    if err != nil { return fmt.Errorf("write failed at offset 0x%x: %w", off, err) }
    if n != len(b) { return fmt.Errorf("short write at offset 0x%x", off) }
    return nil
}

func writeZeroes(f *os.File, off int64, count int) error {
    return writeBytes(f, off, make([]byte, count))
}

func writeFloat(f *os.File, off int64, value float32) error {
    var b [4]byte
    binary.LittleEndian.PutUint32(b[:], math.Float32bits(value))
    return writeBytes(f, off, b[:])
}

func isTimerInitPattern(data []byte) bool {
    off := 0x1c6c20
    marker := []byte{0x68, 0x00, 0x00, 0x70, 0x42, 0x68, 0x00, 0x00, 0x70, 0x42, 0xb9, 0xb8, 0xa4, 0x62, 0x00, 0xe8}
    if len(data) < off+len(marker) { return false }
    return bytes.Equal(data[off:off+len(marker)], marker)
}

func isTimerInitPushSite(data []byte) bool {
    return len(data) > 0x1c6c30 &&
        data[0x1c6c20] == 0x68 &&
        data[0x1c6c25] == 0x68 &&
        data[0x1c6c2a] == 0xb9 &&
        data[0x1c6c2f] == 0xe8
}

func applyFpsPatch(f *os.File, original []byte, fpsValue float32, force bool) error {
    if fpsValue <= 0 { return errors.New("FPS value must be positive") }
    if !isTimerInitPushSite(original) {
        return errors.New("could not find the expected frame-timer initialization site; FPS patch was not applied")
    }
    if !isTimerInitPattern(original) && !force {
        fmt.Println("FPS timer site was found, but it no longer contains the original 60 FPS values; overwriting with requested FPS anyway. Use the .bak file if this was accidental.")
    }
    if err := writeFloat(f, 0x1c6c21, fpsValue); err != nil { return err }
    return writeFloat(f, 0x1c6c26, fpsValue)
}

func findGameExe() string {
    candidates := []string{"Game.exe", filepath.Join("Game", "Game.exe"), "game.exe", filepath.Join("Game", "game.exe")}
    for _, p := range candidates {
        if _, err := os.Stat(p); err == nil { return p }
    }
    return ""
}

func copyBackup(game string) error {
    backup := game + ".bak"
    if _, err := os.Stat(backup); err == nil {
        fmt.Println("Backup already exists:", backup)
        return nil
    }
    in, err := os.Open(game)
    if err != nil { return err }
    defer in.Close()
    out, err := os.OpenFile(backup, os.O_WRONLY|os.O_CREATE|os.O_EXCL, 0666)
    if err != nil { return err }
    _, copyErr := io.Copy(out, in)
    closeErr := out.Close()
    if copyErr != nil { return copyErr }
    if closeErr != nil { return closeErr }
    fmt.Println("Backup created:", backup)
    return nil
}

